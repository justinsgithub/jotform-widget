import Head from "next/head";
// import Link from "next/link";
import Script from "next/script";
import { useState } from "react";

export default function Home() {
  const [lt, setLT] = useState("");
  const [val, setValue] = useState("");
  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Script
        src="https://js.jotform.com/JotFormCustomWidget.min.js"
        strategy="lazyOnload"
        onLoad={() => {
          console.log(`script loaded correctly, window.FB has been populated`);
          //always subscribe to ready event and implement widget related code
          //inside callback function , it is the best practice while developing widgets
          const JF = window.JFCustomWidget;

          JF.subscribe("ready", function () {
            const label = JF.getWidgetSetting("QuestionLabel");

            setLT(label);
            //subscribe to form submit event
            JF.subscribe("submit", function () {
              const msg = {
                //you should valid attribute to data for JotForm
                //to be able to use youw widget as required
                valid: true,
                value: val,
              };

              // send value to JotForm
              JF.sendSubmit(msg);
            });
          });
        }}
      />
      <main>
        <div id="main">
          <h3>This is my first widget.</h3>
          <span>{lt}</span>
          <input
            type="text"
            value={val}
            onChange={(e) => setValue(e.target.value)}
          />
        </div>
      </main>
    </>
  );
}
